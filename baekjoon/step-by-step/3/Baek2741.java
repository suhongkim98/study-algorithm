import java.util.*;
//https://www.acmicpc.net/problem/2741
public class Main{
    
    public static void main(String []args){
        Scanner scanner = new Scanner(System.in);
        int n = scanner.nextInt();
        for(int i = 1 ; i <= n ; i++) {
            System.out.println(i);
        }
        scanner.close();
    }
}
